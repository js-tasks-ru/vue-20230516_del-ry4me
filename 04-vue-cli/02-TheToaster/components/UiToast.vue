<template>
<div class="toast" :class="classname" >
  <UiIcon class="toast__icon" :icon="icon" />
  <span>{{ msg }}</span>

</div>

</template>

<script>
  import UiIcon from './UiIcon.vue';

export default {
  name: 'UiToast',

  components: { UiIcon },

  props: {
    type: {
      type: String,
      required: true
    },
    msg: {
      type: String,
      required: true
    },
  },

  computed: {
    icon() {
      return this.type === 'ok'? 'check-circle': 'alert-circle';
    },
    classname() {
      return this.type === 'ok'? 'toast_success': 'toast_error';
    }
  }

};

</script>


